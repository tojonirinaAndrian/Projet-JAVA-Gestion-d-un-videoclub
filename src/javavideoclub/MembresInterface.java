        genreSupprimerLabel.setText(avoirLesInfo.getGenre());
        
        idHistoriqueLabel.setText(String.valueOf(avoirLesInfo.getId()));
        
        modifierBtn.setEnabled(true);
        supprimerBtn.setEnabled(true);
        historiqueBtn.setEnabled(true);
        
