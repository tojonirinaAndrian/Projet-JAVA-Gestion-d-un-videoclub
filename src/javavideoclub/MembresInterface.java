        int ligne = listeTousLesMembresTable.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)listeTousLesMembresTable.getModel();
        
        DbOperationsMembres avoirLesInfo = new DbOperationsMembres();
        avoirLesInfo.listerLesRecherchesParId(Integer.parseInt(String.valueOf(model.getValueAt(ligne, 0))));
        
