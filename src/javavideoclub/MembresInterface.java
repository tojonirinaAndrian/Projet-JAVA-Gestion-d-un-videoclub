        dateDeNaissanceModifierField.setText(avoirLesInfo.getDate_de_naissance());
        adresseModifierField.setText(avoirLesInfo.getAdresse());
        emailModifierField.setText(avoirLesInfo.getEmail());
        numeroDeTelephoneModifierField.setText(avoirLesInfo.getNumero_de_telephone());
        
