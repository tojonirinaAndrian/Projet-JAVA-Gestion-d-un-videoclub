    private Boolean enabledOrNot () {
        if (titreField.getText().trim().isEmpty()) {
            return false;
        }
        else if (anneeDeSortieField.getText().trim().isEmpty()) {
            return false;
        }
        else if (genreField.getText().trim().isEmpty()){
            return false;
        }
        else {
            if (anneeDeSortieField.getText().trim().length()==4) {
                try {
                    int annee = Integer.parseInt (anneeDeSortieField.getText().trim());
                    return true;
                }
                catch (Exception e) {
                    return false;
                }
            } else {
                return false;
            }
        }     
    }
