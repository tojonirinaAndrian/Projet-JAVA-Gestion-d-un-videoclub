    private void okBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_okBtnMouseClicked
        // TODO add your handling code here:
        okBtn.setEnabled(enabledOrNot());
        try {
            if (okBtn.isEnabled()){
                try {
                    DbOperationsFilms ajoutFilm = new DbOperationsFilms();

                    ajoutFilm.verifierSiExistance(titreField.getText().trim());
                    if (!ajoutFilm.getResult().next()){
                        titreLabel.setText (titreField.getText().trim());
                        anneeDeSortieLabel.setText (anneeDeSortieField.getText().trim());
                        typeLabel.setText (typeChoice.getSelectedItem().trim());
                        genreLabel.setText (genreField.getText().trim());

                        confirmationAjoutPanel.setVisible(true);
                        ajoutFilmPanel.setVisible(false);
                    } else {
                        filmDejaExistantLabel.setText("Film deja existant, veuiller reverifier.");
                        filmDejaExistantLabel.setForeground(Color.decode("#FF3333"));
                    }
                    ajoutFilm.getConnexionALaBdd().close();
                } catch (SQLException e) {e.printStackTrace();}
            }
            else {
                okBtn.setEnabled(enabledOrNot());
            }
        } catch (Exception e) {
            try {
                if (anneeDeSortieField.getText().trim().length() == 4) {
                    int annee = Integer.parseInt(anneeDeSortieField.getText().trim());
                }
            } catch (Exception ev) {
                System.out.println ("Annee Incorrecte");
            }
        }
    }//GEN-LAST:event_okBtnMouseClicked

